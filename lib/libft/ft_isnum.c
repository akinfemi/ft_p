#include "libft.h"

int		ft_isnum(char *s)
{
	intmax_t	num;

	num = ft_atomax(s);
	if (!(num <= 2147483647 && num >= -2147483648) || !s)
		return (0);
	while (s && *s)
	{
		if (!(ft_isdigit(*s) || *s == '-' || *s == '+'))
			return (0);
		s++;
	}
	return (1);
}
