#include "libft.h"

int			ft_bin_to_dec(unsigned int num)
{
	int		res;
	int		i;

	res = 0;
	i = 0;
	while (num != 0)
	{
		res += num % 10 * ft_pow(2, i);
		num /= 10;
		i++;
	}
	return (res);
}
